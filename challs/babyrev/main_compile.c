#include <stdio.h>
#include <string.h>

#define FLAG_LENGTH 43

const char FLAG[] = { 0xf3, 0x6b, 0xb6, 0x4a, 0x63, 0x04, 0x5a, 0xa7, 0x0f, 0xd1, 0x5c, 0x66, 0xe0, 0x90, 0xe5, 0x4a, 0xaf, 0x52, 0x58, 0xb1, 0x45, 0xa1, 0xfc, 0xad, 0xbd, 0x63, 0xc3, 0x66, 0x16, 0x35, 0x15, 0x27, 0xc1, 0x6b, 0xb7, 0x76, 0x3d, 0x4e, 0x52, 0xec, 0x7c, 0x62, 0x91 };
const char KEY[] = { 0x73, 0xfc, 0xb3, 0x0c, 0xfd, 0x4d, 0xf9, 0xa7, 0x09, 0x8b, 0xd7, 0xc1, 0x6b, 0xc9, 0x4a, 0x4a, 0x92, 0xcb, 0xf8, 0xb9, 0x2f, 0xb0, 0x55, 0x92, 0xa8, 0xd6, 0xcb, 0xd1, 0x30, 0x2b, 0x36, 0x0d, 0x61, 0x9a, 0x9d, 0xdd, 0xb4, 0xf1, 0xf9, 0x01, 0xa5, 0x9e, 0xa0 };
unsigned int MAGIC = 0xDEADC0DE;

/*
 * Encrypts a plaintext string in-place.
 * string length must be equal to the flag length
 * if it's not there WILL be an oopsie
 * Args
 *  char*:  plaintext string
 */
void encrypt(char* pt) {
    for(int i = 0; i < FLAG_LENGTH; i++) {
        pt[i] = ((pt[i] ^ KEY[i]) + MAGIC - i);
    }
}

int check_flag(char* inp) {
    encrypt(inp);
    return memcmp(inp, FLAG, FLAG_LENGTH) == 0;
}

int main(int argc, char* argv) {
    puts("What is the flag?");
    char inp[64];
    fgets(inp, sizeof inp, stdin);
    inp[strcspn(inp, "\n")] = 0;
    if(strlen(inp) == FLAG_LENGTH && check_flag(inp)) {
        puts("Correct! You got the flag!");
    }
    else {
        puts("Try again...");
    }
}
